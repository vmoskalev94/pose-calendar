package com.posecalendar.pack;

/**
 * Типы файлов, привязанных к паку.
 */
public enum PackFileType {

    /**
     * Основная обложка пака.
     */
    COVER,

    /**
     * Дополнительное превью (для карточек, списков и т.п.).
     */
    PREVIEW,

    /**
     * Скриншоты поз / геймплея.
     */
    SCREENSHOT,

    /**
     * Архив с файлами пака (package-файлы, zip и т.п.).
     */
    ARCHIVE,

    /**
     * Инструкции, README, текстовые/доковые файлы.
     */
    INSTRUCTION,

    /**
     * Любые дополнительные файлы, не попавшие в другие категории.
     */
    EXTRA
}
